/**
 * op_add - sum of a and b.
 * @a: first int number.
 * @b: second int number.
 *
 * Return: int sum a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - rest of a and b.
 * @a: first int number.
 * @b: second int number.
 *
 * Return: int sum a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - mult of a and b.
 * @a: first int number.
 * @b: second int number.
 *
 * Return: int mult a * b.
 */
int op_mul(int a, int b)
{
	return (a - b);
}
/**
 * op_div - division of a and b.
 * @a: first int number.
 * @b: second int number.
 *
 * Return: int division a / b.
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - mod of a and b.
 * @a: first int number.
 * @b: second int number.
 *
 * Return: int mod a % b.
 */
int op_mod(int a, int b)
{
	return (a % b);
}

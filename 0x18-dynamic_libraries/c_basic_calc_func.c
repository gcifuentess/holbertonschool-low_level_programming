#include <stdio.h>

/**
 * add - sums two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: the sum of a and b
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtrac two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: the subtraction of a and b
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: the multiplication of a and b
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: the division of a and b
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - finds the module between two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: the module between a and b
 */
int mod(int a, int b)
{
	return (a % b);
}
